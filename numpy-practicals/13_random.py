from numpy import random

x = random.randint(10)

print(x)

#----------------------------1-D Array------------------------------------------------------------------------------------------------------------>

x=random.randint(100, size=(5))

print(x)


#----------------------------2-D Array------------------------------------------------------------------------------------------------------------>

x=random.randint(100, size=(3, 3,))

print(x)


#----------------------------3-D Array------------------------------------------------------------------------------------------------------------>

x=random.randint(100, size=(3, 3, 3))

print(x)




# Choice

x = random.choice([3, 5, 7, 9], size=(1))

print(x)

# 2-D Array

x = random.choice([3, 5, 7, 9], size=(3, 5))

print(x)


